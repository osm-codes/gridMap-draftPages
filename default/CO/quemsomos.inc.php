    <h1>Quem Somos</h1>
    <p>
      O Instituto AddressForAll tem suas ações guiadas pela defesa dos dados
      abertos, da descentralização, do conhecimento livre, da qualidade da
      informação geoespacial e da aplicação da tecnologia como ferramenta
      para uma sociedade justa — contribuindo na redução das desigualdades e
      na proteção e assistência social e ambiental.
    </p>
    <p>
      <dl>
        Pelo seu estatuto, o Instituto AddressForAll tem como objetivo:
        <dd>1.1Promover a abertura, fusão, construção colaborativa e padronização de dados e metadados geoespaciais relativos a endereços e elementos relacionados.</dd>
        <dd>2.2. Fomentar as iniciativas de Software livre.</dd>
        <dd>2.3. Difundir, orientar, monitorar, avaliar e auditar entes públicos e privados no que tange a LGPD.</dd>
        <dd>2.4. Elaborar, gerir, sediar, monitorar, avaliar e auditar bases de dados sensíveis de interesse público, sejam elas de pessoas, atividades ou relações complexas entre atores que demandem tais informações.</dd>
        <dd>2.5. Difundir o conhecimento tecnológico.</dd>
        <dd>2.6. Desenvolver e apoiar comunidades de dados.</dd>
        <dd>2.7. Realizar a curadoria, tutela e preservação digital dos dados adquiridos ou gerados pelo Instituto.</dd>
        <dd>2.8. Apoiar a formação e manutenção de infraestrutura de acesso aos dados e seu processamento, bem como certificação de integridade e registro formal da sua licença e proveniência.</dd>
        <dd>2.9. Desenvolver a assistência social sob a perspectiva das novas tecnologias sociais.</dd>
        <dd>2.10. Desenvolver Proteção, conservação e recuperação ambiental sob os pilares da tecnologia e desenvolvimento econômico.</dd>
        <dd>2.11. Promover e incentivar pesquisas tecnológicas relacionadas aos objetivos do Instituto.</dd>
      </dl>
    </p>
